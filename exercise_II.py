listOfStudents = ['Tom', 'Sam', 'Vlad', 'Liisa']

if len(listOfStudents) % 2 == 0:   
    print(f"The number of students ({len(listOfStudents)}) is even and they can be split into a groups of two")
else:
    print(f"The number of students ({len(listOfStudents)}) is odd and they need to be devided in a groups of three")